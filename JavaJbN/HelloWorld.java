
public class HelloWorld{
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        int c = a+b;
        int d = a-b;
        int e = a*b;
        int f = a/b;
        int g = a%b;

        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);

        int z = 10;
        System.out.println(++z);
        System.out.println(z);

        // RELATIONAL OPERATORS

        
    }
}